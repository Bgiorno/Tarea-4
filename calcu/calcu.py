def add(a,b):
    """
    Se retorna la suma de a y b

    Args:
        a (float): Numero
        b (float): Segundo numero

    Returns:
         Regresa la suma de los numeros a y b
    
    Examples:
        >>> add(2,3)
        >>> 5
        .. image:: ../docs/img/Naruto_I.jpg
            :aling: center
    """
    return float(a+b)


def res(a,b):
    """
    Se retorna la resta de a y b

    Args:
        a (float): Numero
        b (float): Segundo numero


    Returns:
        Regresa la resta de los numeros a y b

    Examples:
        >>> res(5,3)
        >>> 3

    """
    return float(a-b)
